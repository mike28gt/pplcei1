# Operadores relaciones
#   Su principal característica es que su resultado es un dato de tipo lógico.
#   
#       Operador mayor que (>): verifica si el número de la izquierda es mayor que el número de la derecha.
#       Operador menor que (<): verifica si el número de la izquierda es menor que el número de la derecha.
#       Operador mayor o igual que (>=): verifica si el número de la izquierda es mayor o igual que el número de la derecha.
#       Operador menor o igual que (<=): verifica si el número de la izquierda es menor o igual que el número de la derecha.
#       Operador igual que (==): verifica si el número de la izquierda es igual que el número de la derecha (aplica para otros tipos de datos)
#       Operador diferente que (!=) = verifica si el número de la izquierda es diferente que el número de la derecha (aplica para otros tipos de datos)
#
#
# Precedencia de operadores aritméticos
#    Nota: cuando se tienen operadores con la misma precedencia en una expresión se deben resolver de izquierda a derecha.
#    1. Paréntesis
#    2. Potencia
#    3. Multiplicación, División, Módulo
#    4. Suma, Resta
#    5. Mayor que, Menor que, Mayor o igual que, Menor o igual, Igual que, Diferente que
#
