# Los tipos de datos en Python se dividen en
#   Número
#       Se representan por medio del número tal cual.
#       Se pueden representar números enteros y datos reales.     
#   Texto
#       Requiere de delimitador de inicio y final.
#       Sus delimitadores son: "" y/o ''
#   Lógico
#       Únicamente puede tener el valor de verdadero o falso de forma excluyente.
#       True / False

# Tipos de operaciones con datos numéricos
#   suma: su operador es el símbolo +
#   resta: su operador es el símbolo de -
#   multiplicación: su operador es el símbolo *
#   división: su operador es el símbolo /
#   potencia: su operador es el símbolo **
#   residuo o módulo: su operador es el símbolo %


