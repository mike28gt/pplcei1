# Se obtiene la base
base = int(input("Ingrese la base de la caja: "))

# Se obtiene la altura
altura = int(input("Ingrese la altura de la caja: "))

# Se obtiene la profundidad
profundidad = int(input("Ingrese la profundidad de la caja: "))

# Se calcula volumen
volumen = base * altura * profundidad

# Se muestra el resultado
print("El volumen de la caja es:", volumen)



